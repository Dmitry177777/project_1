def upper_string (str_i):
 """ Поворот строки с заглавными буквами """
 str_result = str (str_i)
 return str_result.upper()

def capital_string (str_i):
 """ Возврат строки с первыми заглавными буквами """
 str_result = str(str_i)
 return str_result.capitalize()

